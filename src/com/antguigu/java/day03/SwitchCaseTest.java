package com.antguigu.java.day03;

public class SwitchCaseTest {

    public static  void main(String[] args){
        int numberi = 2;
        switch (numberi){
            case  0:
                System.out.println("zero");
                break;
            case  1:
                System.out.println("one");
                break;
             default:
                 System.out.println("AAA");
                 break;
        }
    }
}
